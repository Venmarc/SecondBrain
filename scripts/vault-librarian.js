const fs = require('fs');
const path = require('path');

// Portability: Resolve VAULT_ROOT relative to the script location
const VAULT_ROOT = path.resolve(__dirname, '..');
const SESSIONS_DIR = path.join(VAULT_ROOT, '06-Agent-Sessions');
const HISTORY_FILE = path.join(SESSIONS_DIR, 'session-history.json');

function getTodayString() {
  const date = new Date();
  const yyyy = date.getFullYear();
  const mm = String(date.getMonth() + 1).padStart(2, '0');
  const dd = String(date.getDate()).padStart(2, '0');
  return `${yyyy}-${mm}-${dd}`;
}

// Ensure the directory exists
try {
  if (!fs.existsSync(SESSIONS_DIR)) {
    fs.mkdirSync(SESSIONS_DIR, { recursive: true });
  }
} catch (err) {
  console.error("Error creating sessions directory:", err.message);
}

// Load session history
let history = { askedQuestionIds: [], lastSessionDate: "" };
try {
  if (fs.existsSync(HISTORY_FILE)) {
    const data = fs.readFileSync(HISTORY_FILE, 'utf8');
    history = JSON.parse(data);
  }
} catch (err) {
  console.error("Warning: Failed to load session history. Starting fresh.", err.message);
}

// Handle --record command
const args = process.argv.slice(2);
if (args.includes('--record')) {
  const index = args.indexOf('--record');
  const recordIdsString = args[index + 1];
  if (recordIdsString) {
    const idsToRecord = recordIdsString.split(',').map(id => id.trim()).filter(Boolean);
    history.askedQuestionIds = [...new Set([...history.askedQuestionIds, ...idsToRecord])];
    history.lastSessionDate = getTodayString();
    try {
      fs.writeFileSync(HISTORY_FILE, JSON.stringify(history, null, 2), 'utf8');
      console.log(`Successfully recorded ${idsToRecord.length} question(s) in history.`);
      
      // Also generate session markdown log
      const today = getTodayString();
      const sessionLogFile = path.join(SESSIONS_DIR, `session-${today}.md`);
      const logContent = `# Agent Interview Session Log - ${today}
> **One-line Summary**: Automatically generated summary of answered interview questions for this session.

- **Date**: ${today}
- **Recorded Question IDs**:
${idsToRecord.map(id => `  - \`${id}\``).join('\n')}

---
*Generated by Vault Librarian Interviewer System.*
`;
      fs.writeFileSync(sessionLogFile, logContent, 'utf8');
      console.log(`Saved session log to 06-Agent-Sessions/session-${today}.md`);
    } catch (err) {
      console.error("Error saving history or session log:", err.message);
    }
  } else {
    console.error("Error: --record flag requires comma-separated question IDs.");
  }
  process.exit(0);
}

// Scanners
function scanInbox() {
  const items = [];
  const inboxDir = path.join(VAULT_ROOT, '00-Inbox');
  const clippingsDir = path.join(VAULT_ROOT, 'Clippings');
  
  const scanDir = (dir) => {
    try {
      if (fs.existsSync(dir)) {
        fs.readdirSync(dir).forEach(file => {
          if (file !== '.gitkeep' && !file.startsWith('.')) {
            const fullPath = path.join(dir, file);
            const relativePath = path.relative(VAULT_ROOT, fullPath);
            let ageHours = 0;
            try {
              const stats = fs.statSync(fullPath);
              ageHours = (Date.now() - stats.mtimeMs) / (1000 * 60 * 60);
            } catch (statErr) {
              // ignore stat error, fallback to age 0
            }
            items.push({ relativePath, ageHours });
          }
        });
      }
    } catch (err) {
      console.error(`Error scanning directory ${dir}:`, err.message);
    }
  };

  scanDir(inboxDir);
  scanDir(clippingsDir);
  return items;
}

function parseLintReport() {
  const orphans = [];
  const missingDocs = [];
  const missingSummaries = [];
  const lintReportPath = path.join(VAULT_ROOT, 'LINT-REPORT.md');

  try {
    if (fs.existsSync(lintReportPath)) {
      const content = fs.readFileSync(lintReportPath, 'utf8');

      // Robust split-by-header or section parser instead of fragile regex
      const sections = content.split('\n## ');
      sections.forEach(sec => {
        if (sec.toLowerCase().includes('orphan notes')) {
          const links = sec.match(/\[\[(.*?)\]\]/g);
          if (links) {
            links.forEach(l => {
              const clean = l.replace(/[\[\]]/g, '');
              orphans.push(clean);
            });
          }
        } else if (sec.toLowerCase().includes('9-doc standard gaps')) {
          const lines = sec.split('\n');
          lines.forEach(line => {
            if (line.includes('|') && !line.includes('Project Name') && !line.includes(':---')) {
              const parts = line.split('|').map(p => p.trim());
              if (parts.length >= 3) {
                const projName = parts[1].replace(/\*\*/g, '');
                const types = parts[2].split(',').map(t => t.trim()).filter(Boolean);
                if (projName && types.length > 0) {
                  missingDocs.push({ project: projName, types });
                }
              }
            }
          });
        } else if (sec.toLowerCase().includes('style consistency gaps')) {
          const links = sec.match(/\[\[(.*?)\]\]/g);
          if (links) {
            links.forEach(l => {
              const clean = l.replace(/[\[\]]/g, '');
              missingSummaries.push(clean);
            });
          }
        }
      });
    }
  } catch (err) {
    console.error("Error parsing LINT-REPORT.md:", err.message);
  }

  return { orphans, missingDocs, missingSummaries };
}

function checkDailyNote() {
  const today = getTodayString();
  const dailyPath = path.join(VAULT_ROOT, '05-Daily', `${today}.md`);
  try {
    if (fs.existsSync(dailyPath)) {
      const stats = fs.statSync(dailyPath);
      // Daily note check: check if file is larger than 50 bytes (practically empty)
      if (stats.size > 50) {
        return true;
      }
    }
  } catch (err) {
    console.error("Error checking daily note:", err.message);
  }
  return false;
}

function generateQuestions() {
  const inboxItems = scanInbox();
  const { orphans, missingDocs, missingSummaries } = parseLintReport();
  const dailyExists = checkDailyNote();

  const candidates = [];

  // 1. Inbox Items with priority based on age (>48 hours)
  inboxItems.forEach(item => {
    const isStale = item.ageHours > 48;
    const score = isStale ? 100 : 70;
    const timeLabel = isStale ? "stale (older than 48 hours)" : "new";
    candidates.push({
      id: `inbox:${item.relativePath}`,
      score,
      text: `I see a ${timeLabel} unprocessed file in your inbox/clippings: \`${item.relativePath}\`. What key insights or learnings should we extract and summarize from it?`
    });
  });

  // 2. Project Gaps (Missing 9-doc files) - Score 90
  missingDocs.forEach(target => {
    target.types.forEach(docType => {
      candidates.push({
        id: `project-gap:${target.project}:${docType}`,
        score: 90,
        text: `For project [[${target.project}]], we are missing the core specification file [[${docType}]]. Would you like to outline or brainstorm the requirements for it today?`
      });
    });
  });

  // 3. Daily Reflection - Score 80
  if (!dailyExists) {
    candidates.push({
      id: `daily-reflection:${getTodayString()}`,
      score: 80,
      text: `I noticed today's daily log is empty or hasn't been created yet (${getTodayString()}). What was your primary focus, biggest victory, or core blocker during today's sessions?`
    });
  }

  // 4. Orphans - Score 60
  if (orphans.length > 0) {
    // Exclude certain paths like archives or specific files
    const cleanOrphans = orphans.filter(o => !o.includes('Momentum-original') && !o.includes('04-Archive'));
    cleanOrphans.forEach(orphan => {
      candidates.push({
        id: `orphan:${orphan}`,
        score: 60,
        text: `Note [[${orphan}]] is currently an orphan with no incoming links. Where does this fit in your knowledge graph, or what other notes should link to it?`
      });
    });
  }

  // 5. Missing Summaries - Score 50
  if (missingSummaries.length > 0) {
    const cleanSummaries = missingSummaries.filter(s => !s.includes('04-Archive'));
    cleanSummaries.forEach(summary => {
      candidates.push({
        id: `missing-summary:${summary}`,
        score: 50,
        text: `Note [[${summary}]] is missing a 'One-line Summary' at the top. Can you summarize the purpose or core takeaway of this note in one clear sentence so we can update it?`
      });
    });
  }

  // 6. Fallback General Questions - Score 10
  candidates.push(
    { id: 'fallback:tempire-uiux', score: 10, text: `What is the current biggest UI/UX friction or "this doesn't feel premium yet" feeling you're experiencing in [[Tempire]] right now?` },
    { id: 'fallback:momentum-habits', score: 10, text: `Have there been any shifts in your habit/fitness operating system vision for [[Momentum]] since our last PRD update?` },
    { id: 'fallback:new-skills', score: 10, text: `What is one new skill, mental model, or design pattern you've learned recently that we should document as a permanent resource in your vault?` },
    { id: 'fallback:crazy-idea', score: 10, text: `What "crazy" or half-baked idea have you had lately (in software, marketing, or content creation) that we should capture even if raw?` },
    { id: 'fallback:neglected-area', score: 10, text: `Looking at your ongoing responsibilities (Personal Growth, Content Creation), is there an area that has felt neglected lately? What's one action we can plan to revive it?` }
  );

  // Filter candidates using asked history
  const askedSet = new Set(history.askedQuestionIds || []);
  let filtered = candidates.filter(c => !askedSet.has(c.id));

  // Sort by priority score descending
  filtered.sort((a, b) => b.score - a.score);

  // Rotation: If we have fewer than 3 questions left, prune oldest entries in history to recycle
  if (filtered.length < 3) {
    const pruneCount = Math.floor(history.askedQuestionIds.length / 2);
    history.askedQuestionIds = history.askedQuestionIds.slice(pruneCount);
    const updatedAskedSet = new Set(history.askedQuestionIds);
    filtered = candidates.filter(c => !updatedAskedSet.has(c.id));
    filtered.sort((a, b) => b.score - a.score);
    
    try {
      fs.writeFileSync(HISTORY_FILE, JSON.stringify(history, null, 2), 'utf8');
    } catch (err) {
      console.error("Warning: Failed to update history on rotation.", err.message);
    }
  }

  // Limit to exactly 3 to 7 questions
  return filtered.slice(0, 7);
}

const selected = generateQuestions();
if (args.includes('--json')) {
  console.log(JSON.stringify(selected, null, 2));
} else {
  console.log('### Vault Librarian Interview Questions\n');
  selected.forEach((q, index) => {
    console.log(`${index + 1}. ${q.text} <!-- id: ${q.id} -->\n`);
  });
}
